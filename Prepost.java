class Prepost {
    public static void main(String[] args) {
        //pre หน้า post หลัง

        int a=5;

        System.out.println(a);
        System.out.println(++a); //บวกก่อนใช้ = 6
        System.out.println(a);

        int b=5;
        System.out.println(b);
        System.out.println(b++); //ใช้ 5 ก่อน
        System.out.println(b); //บวกเพิ่มตัวนี้
    }
}
