class Compare {
    public static void main(String[] args) {
        //true/false boolean
        int a=10,b=20;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b); //OR true แค่ 1 => true
    }
}
